#include<stdio.h>
#include<string.h>
#include<stdlib.h>
int main() {
	long  int i;
	scanf("%ld", &i);
	if (i <= 100000) {
		printf("%.2f", i * (1.1));
	}
	else if(i<=200000) {
		printf("%.2f", 100000 * 1.1 + (i - 100000) * 1.075);
	}
	else if (i<=400000) {
		printf("%.2f",100000*1.1+100000*1.075+(i-200000)*1.05);
	}
	else if (i<=600000) {
		printf("%.2f",100000*1.1+100000*1.075+200000*1.05+(i-400000)*1.03);
	}
	else if (i<=1000000) {
		printf("%.2f", 100000 * 1.1 + 100000 * 1.075 + 200000 * 1.05 + 200000 * 1.03 +(i-600000)*1.015 );
	}
	else {
		printf("%.2f", 100000 * 1.1 + 100000 * 1.075 + 200000 * 1.05 + 200000 * 1.03 + 400000 * 1.015+(i-1000000)*1.01);
	}
	return 0;

}
